const doMath = function(num1, num2, operator){
    let total;
    if(operator === "+") {
        total = num1 + num2
    }
    if(operator === "-") {
        total = num1 - num2
    }
    if(operator === "*") {
        total = num1 * num2
    }
    if(operator === "/") {
        total = num1 / num2
    }

}

